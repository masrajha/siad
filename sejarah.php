                            <h4>Sejarah Desa Fajar Baru</h4>
                            <p style="text-align: justify;">Awal mulanya pada tahun 1930 an adalah suatu wilayah hutan belantara yang kemudian masuklah 7 orang yang berasal dari suku OKU (Ogan Komering Ulu) untuk membukan hutan tersebut dimana ketujuh orang tersebut dipimpin oleh Hi. Prabu Kamil selaku tokoh adat dan kemudian orang-orang dari suku OKU berdatangan ikut membuka hutan tersebut, serta pada saat itu belumlah terbentuk suatu wilayah kepemerintahan desa, maka para tokoh tersebut memberikan dama kepada wilayah tersebut dengan nama Fajar Baru dan wilayah tersebut masih berstatus tanah marga.</p>
                            <p style="text-align: justify;">Menurut riwayat wilayah Fajar Baru merupakan daerah pertahanan rakyat pada jaman Belanda. yang akhirnya tentara Belanda dapat diusir dari wilayah Fajar Baru kemudian pada tahun 1939 berulah dibentuk suatu tatanan kepemerintahan Desa, adapun beberapa Kepala Desa yang pernah menjabat di Desa Fajar Baru yaitu:</p>
                            <p style="text-align: justify;">Berikut adalah nama – nama Kepala Desa yang pernah memimpin desa Fajar Baru :</p>
                            <ul>
                                <li>RAJA ASAL, 1939 s.d 1950</li>
                                <li>M. YUSUF TUMENGGUNG, 1950 s.d 1960</li>
                                <li>M. TOHA, 1960 s.d 1970</li>
                                <li>TAUFIK YUSUF, 1976 s.d 1984</li>
                                <li>ASNAWI KENCANA, 1984 s.d 1992</li>
                                <li>THAMRIN JAILANI, 1992 s.d 1994</li>
                                <li>DANYANI, 1994 s.d 1997</li>
                                <li>ABDULAH, 1998 s.d 1999</li>
                                <li>TARMUJI, 1999 s.d 2001</li>
                                <li>SAMSUDIN, 2001 s.d 2002</li>
                                <li>ZULKARNAIK HZ., 2003 s.d 2013</li>
                                <li>ARJUNA, 2013 s.d 2019</li>
                            </ul><br>
                            <p style="text-align: justify;">Desa Fajar Baru merupakan salah satu Desa di Kecamatan Pagelaran Utara Kabupaten Pringsewu Provinsi Lampung, memiliki luas ± 5.650 Ha. Secara geografis Desa Fajar Baru berbatasan dengan wilayah sebagai berikut:</p>
                            <ul>
                                <li>Sebelah Utara       : Desa Fajar Mulya Kec. Pagelaran Utara</li>
                                <li>Sebelah Selatan : Desa Pasir Ukir dan Lugusari Kec. Pagelaran</li>
                                <li>Sebelah Barat       : Desa Kamilin dan Fajar Mulya Kec. Pagelaran Utara</li>
                                <li>Sebelah Timur       : Desa Banjarejo Kec. Banyumas dan Sinar Mulya Kec. Pagelaran Utara</li>
                            </ul><br>
                            <p style="text-align: justify;">Secara administratif, wilayah Desa Fajar Baru terdiri dari 5 dusun dan 8 rukun tetangga. Secara umum Tipologi Desa Fajar Baru terdiri dari persawahan, perladangan, perkebunan, perikanan. Secara Topografis Desa Fajar Baru secara umum termasuk daerah dataran sedang yang bergelombang, dan berdasarkan ketinggian wilayah Desa Fajar Baru diklasifikasikan sebagai wilayah dengan dataran sedang (≥100-500m dpl).</p>
